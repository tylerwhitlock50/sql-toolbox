    INNER JOIN [ESI_CORE].[ESI_DOCUMENT_TYPE] EDI_DOT WITH (NOLOCK) ON EDI_DOT.[DOCUMENT_TYPE] = EDI_DCT.[DOCUMENT_TYPE]
    AND EDI_DOT.[EDI_DIRECTION] = EDI_DCT.[EDI_DIRECTION]
