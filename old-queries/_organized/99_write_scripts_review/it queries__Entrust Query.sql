    LEFT JOIN [ESI_EDI].[ESI_DASH_HEADER_INVOICE] EDI_INH WITH (READCOMMITTED) ON EDI_INH.[INVOICE_ID] = ERP_INH.[INVOICE_ID]
    LEFT JOIN [ESI_EDI].[ESI_DASH_HEADER] EDI_ORH WITH (READCOMMITTED) ON EDI_ORH.[ESI_DASH_HEADER_ID] = EDI_INH.[ESI_DASH_HEADER_ID]
    AND EDI_ORH.[DOCUMENT_TYPE] = '810'
    AND EDI_ORH.[EDI_DIRECTION] = 'O'
    AND ISNULL(EDI_ORH.[SITE_ID], '') = ISNULL(ERP_INH.[SITE_ID], '')
    INNER JOIN [ESI_CORE].[ESI_CUSTOMER] EDI_CST WITH (NOLOCK) ON EDI_CST.[ERP_CUSTOMER_ID] = ERP_INH.[ERP_CUSTOMER_ID]
    AND ISNULL(EDI_CST.[SITE_ID], '') = ISNULL(ERP_INH.[SITE_ID], '')
    INNER JOIN [ESI_CORE].[ESI_CUSTOMER_DOCUMENT_TYPE] EDI_DCT WITH (NOLOCK) ON EDI_DCT.[ESI_CUSTOMER_ID] = EDI_CST.[ESI_CUSTOMER_ID]
    INNER JOIN [ESI_CORE].[ESI_DOCUMENT_TYPE] EDI_DOT WITH (NOLOCK) ON EDI_DOT.[DOCUMENT_TYPE] = EDI_DCT.[DOCUMENT_TYPE]
    AND EDI_DOT.[EDI_DIRECTION] = EDI_DCT.[EDI_DIRECTION]
    INNER JOIN [ESI_EDI].[ESI_DASH_HEADER] EDI_ORH_I WITH (NOLOCK) ON EDI_ORH_I.[CUSTOMER_ORDER_ID] = ERP_INH.[CUSTOMER_ORDER_ID]
    AND ISNULL(EDI_ORH_I.[SITE_ID], '') = ISNULL(ERP_INH.[SITE_ID], '')
    AND EDI_ORH_I.[DOCUMENT_TYPE] IN ('830', '850', '860', '862')
    AND EDI_ORH_I.[EDI_DIRECTION] = 'I'
    AND EDI_ORH_I.[EDI_STATUS] = 'A'
    AND EDI_ORH_I.[CUSTOMER_ORDER_ID] <> '0'
WHERE
    ERP_INH.[TYPE] = 'I'
    AND CONVERT(DATE, ERP_INH.[INVOICE_DATE]) >= CONVERT(DATE, EDI_DCT.[ACTIVE_DATE])
    AND EDI_INH.[INVOICE_ID] IS NULL
    AND EDI_DCT.SEND_NON_EDI = '0'
    AND EDI_DCT.[DOCUMENT_TYPE] = '810'
    AND EDI_DCT.[EDI_DIRECTION] = 'O'
    AND EDI_DCT.[DELETE_DATE] IS NULL
    AND EDI_CST.[DELETE_DATE] IS NULL
    AND EDI_DOT.[IS_ENABLED] = '1' --Get using possible original order. This is for backorders.--       
INSERT INTO
    @tbl_RECEIVABLE (
        [INVOICE_ID],
        [SITE_ID],
        [PLANT_ID],
        [WAREHOUSE_ID]
    )
SELECT
    DISTINCT ERP_INH.[INVOICE_ID],
    ERP_INH.[SITE_ID],
    ERP_INH.[PLANT_ID],
    ERP_INH.[WAREHOUSE_ID]
FROM
    [ESI_CORE].[v_ERP_INVOICE_HEADER] ERP_INH WITH (NOLOCK)
    LEFT JOIN [ESI_EDI].[ESI_DASH_HEADER_INVOICE] EDI_INH WITH (READCOMMITTED) ON EDI_INH.[INVOICE_ID] = ERP_INH.[INVOICE_ID]
    LEFT JOIN [ESI_EDI].[ESI_DASH_HEADER] EDI_ORH WITH (READCOMMITTED) ON EDI_ORH.[ESI_DASH_HEADER_ID] = EDI_INH.[ESI_DASH_HEADER_ID]
    AND EDI_ORH.[DOCUMENT_TYPE] = '810'
    AND EDI_ORH.[EDI_DIRECTION] = 'O'
    AND ISNULL(EDI_ORH.[SITE_ID], '') = ISNULL(ERP_INH.[SITE_ID], '')
    INNER JOIN [ESI_CORE].[ESI_CUSTOMER] EDI_CST WITH (NOLOCK) ON EDI_CST.[ERP_CUSTOMER_ID] = ERP_INH.[ERP_CUSTOMER_ID]
    AND ISNULL(EDI_CST.[SITE_ID], '') = ISNULL(ERP_INH.[SITE_ID], '')
    INNER JOIN [ESI_CORE].[ESI_CUSTOMER_DOCUMENT_TYPE] EDI_DCT WITH (NOLOCK) ON EDI_DCT.[ESI_CUSTOMER_ID] = EDI_CST.[ESI_CUSTOMER_ID]
    INNER JOIN [ESI_CORE].[ESI_DOCUMENT_TYPE] EDI_DOT WITH (NOLOCK) ON EDI_DOT.[DOCUMENT_TYPE] = EDI_DCT.[DOCUMENT_TYPE]
    AND EDI_DOT.[EDI_DIRECTION] = EDI_DCT.[EDI_DIRECTION]
    INNER JOIN [ESI_EDI].[ESI_DASH_HEADER] EDI_ORH_I WITH (NOLOCK) ON EDI_ORH_I.[CUSTOMER_ORDER_ID] = SUBSTRING(
        CONVERT(VARCHAR(20), ERP_INH.[CUSTOMER_ORDER_ID]),
        0,
        CASE
            WHEN CHARINDEX(
                '-',
                (CONVERT(VARCHAR(20), ERP_INH.[CUSTOMER_ORDER_ID]))
            ) > 0 THEN CHARINDEX(
                '-',
                (CONVERT(VARCHAR(20), ERP_INH.[CUSTOMER_ORDER_ID]))
            )
            ELSE LEN(ERP_INH.[CUSTOMER_ORDER_ID]) + 1
        END
    )
    AND ISNULL(EDI_ORH_I.[SITE_ID], '') = ISNULL(ERP_INH.[SITE_ID], '')
    AND EDI_ORH_I.[DOCUMENT_TYPE] IN ('830', '850', '860', '862')
    AND EDI_ORH_I.[EDI_DIRECTION] = 'I'
    AND EDI_ORH_I.[EDI_STATUS] = 'A'
    AND EDI_ORH_I.[CUSTOMER_ORDER_ID] <> '0'
WHERE
    ERP_INH.[TYPE] = 'I'
    AND CONVERT(DATE, ERP_INH.[INVOICE_DATE]) >= CONVERT(DATE, EDI_DCT.[ACTIVE_DATE])
    AND EDI_INH.[INVOICE_ID] IS NULL
    AND EDI_DCT.SEND_NON_EDI = '0'
    AND EDI_DCT.[DOCUMENT_TYPE] = '810'
    AND EDI_DCT.[EDI_DIRECTION] = 'O'
    AND EDI_DCT.[DELETE_DATE] IS NULL
    AND EDI_CST.[DELETE_DATE] IS NULL
    AND EDI_DOT.[IS_ENABLED] = '1'
SELECT
    DISTINCT [INVOICE_ID],
    [SITE_ID],
    [PLANT_ID],
    [WAREHOUSE_ID]
FROM
    @tbl_RECEIVABLE
END
END
