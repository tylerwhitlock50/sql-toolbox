    ERP_INH.[TYPE] = 'I'
    AND CONVERT(DATE, ERP_INH.[INVOICE_DATE]) >= CONVERT(DATE, EDI_DCT.[ACTIVE_DATE])
    AND EDI_INH.[INVOICE_ID] IS NULL
