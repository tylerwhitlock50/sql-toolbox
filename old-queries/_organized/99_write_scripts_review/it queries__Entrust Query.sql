    LEFT JOIN [ESI_EDI].[ESI_DASH_HEADER] EDI_ORH WITH (READCOMMITTED) ON EDI_ORH.[ESI_DASH_HEADER_ID] = EDI_INH.[ESI_DASH_HEADER_ID]
    AND EDI_ORH.[DOCUMENT_TYPE] = '810'
    AND EDI_ORH.[EDI_DIRECTION] = 'O'
    AND ISNULL(EDI_ORH.[SITE_ID], '') = ISNULL(ERP_INH.[SITE_ID], '')
    INNER JOIN [ESI_CORE].[ESI_CUSTOMER] EDI_CST WITH (NOLOCK) ON EDI_CST.[ERP_CUSTOMER_ID] = ERP_INH.[ERP_CUSTOMER_ID]
